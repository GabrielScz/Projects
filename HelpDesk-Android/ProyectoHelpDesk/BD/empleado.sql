DROP PROCEDURE IF EXISTS insertarEmpleado;
DELIMITER $$
CREATE PROCEDURE insertarEmpleado(    /* Datos Personales */
                                    IN    var_nombreEmpleado       	VARCHAR(40),    --  1
                                    IN    var_primerApellido 		VARCHAR(40),    --  2
                                    IN    var_segundoApellido 		VARCHAR(40),    --  3
                                    IN    var_telefono          	VARCHAR(20),    --  4
                                    IN    var_email					VARCHAR(40),    --  5
                                    IN    var_rfc  	         		VARCHAR(15),    --  6
                                    IN    var_fotografia   		    LONGTEXT,       --  7
                                    
                                    /* Datos de Usuario */
                                    IN    var_nombreUsuario   		VARCHAR(129),   --  8
                                    IN    var_contrasenia     		VARCHAR(129),   --  9
                                    IN    var_rol             		VARCHAR(25),    --  10 
                                    IN    var_idDepartamento		INT, 			--  11
                                    
                                    /* Valores de Retorno */
                                    OUT    var_idEmpleado      		INT,            --  12
                                    OUT    var_idUsuario       		INT             --  13
                                    
                )                                    
    BEGIN        
        -- Insertamos los datos de Usuario:
        INSERT INTO usuario (nombreUsuario, contrasenia, rol) 
                     VALUES (var_nombreUsuario, var_contrasenia, var_rol);
		
        -- Obtenemos el ID de Usuario que se generó:
        SET var_idUsuario = LAST_INSERT_ID(); 
        
        -- Comenzamos insertando los datos de la Persona:
        INSERT INTO empleado (nombreEmpleado, primerApellido, segundoApellido, telefono,
                              email, rfc, fotografia, idUsuario, idDepartamento)
                      VALUES (var_nombreEmpleado, var_primerApellido, var_segundoApellido, 
							  var_telefono, var_email, var_rfc, var_fotografia,var_idUsuario, var_idDepartamento);
                              
        -- Obtenemos el ID de Persona que se generó:
        SET var_idEmpleado = LAST_INSERT_ID();
    END
$$
DELIMITER ;  

